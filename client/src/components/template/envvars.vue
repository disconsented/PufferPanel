<template>
  <div>
    <v-row
      v-for="(env, i) in value"
      :key="i"
    >
      <v-col
        cols="4"
        class="my-0 py-0"
      >
        <v-subheader v-text="i" />
      </v-col>
      <v-col
        cols="8"
        class="my-0 py-0"
      >
        <ui-input
          v-model="value[i]"
          dense
          hide-details
          icon-behind="mdi-close-circle"
          @click:append-outer="delete value[i]; $forceUpdate()"
        />
      </v-col>
    </v-row>
    <v-btn
      v-if="!addingEnvVar"
      text
      block
      @click="addingEnvVar = true"
      v-text="$t('templates.AddEnvVar')"
    />
    <v-row v-else>
      <v-col
        cols="12"
        md="6"
      >
        <ui-input
          v-model="newEnvVar"
          :label="$t('common.Name')"
          dense
          hide-details
        />
      </v-col>
      <v-col
        cols="6"
        md="3"
      >
        <v-btn
          color="primary"
          block
          @click="addEnvVar()"
          v-text="$t('templates.AddEnvVar')"
        />
      </v-col>
      <v-col
        cols="6"
        md="3"
      >
        <v-btn
          color="error"
          block
          @click="newEnvVar = ''; addingEnvVar = false"
          v-text="$t('common.Cancel')"
        />
      </v-col>
    </v-row>
  </div>
</template>

<script>
export default {
  props: {
    value: { type: Object, default: () => {} }
  },
  data () {
    return {
      addingEnvVar: false,
      newEnvVar: ''
    }
  },
  methods: {
    addEnvVar () {
      if (this.newEnvVar.trim().length > 0 && this.newEnvVar.trim().indexOf(' ') === -1) {
        this.value[this.newEnvVar.trim()] = ''
        this.newEnvVar = ''
        this.addingEnvVar = false
      } else {
        this.$toast.error(this.$t('templates.VarNameNoSpaces'))
      }
    }
  }
}
</script>
